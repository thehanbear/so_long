int	ft_strrcmp(char *s1, char *s2)
{
	int	i;
	int	j;

	i = 0;
	j = ft_strlen(s1) - ft_strlen(s2);
	while (s1[j] == s2[i] && s1[j] != '\0')
	{
		i++;
		j++;
	}
	if (s1[j] == s2[i])
		return (0);
	else
		return (s1[j] - s2[i]);
}
