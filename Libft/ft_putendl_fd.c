#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s != 0)
	{
		while (s[i])
		{
			write (fd, s, 1);
			s++;
		}
		write (fd, "\n", 1);
	}
}
